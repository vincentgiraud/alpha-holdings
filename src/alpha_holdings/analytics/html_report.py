"""HTML performance report generation.

Produces a self-contained HTML file with performance metrics, NAV chart,
drawdown chart, factor attribution, and weight history visualization.
All charts are rendered as inline SVG — no external dependencies.
"""

from __future__ import annotations

from pathlib import Path

import numpy as np
import pandas as pd

from alpha_holdings.analytics.attribution import AttributionResult
from alpha_holdings.analytics.performance import PerformanceReport


def render_html_report(
    *,
    report: PerformanceReport,
    nav_series: pd.DataFrame,
    attribution: AttributionResult | None = None,
    weight_history: pd.DataFrame | None = None,
    output_path: Path,
) -> Path:
    """Render a self-contained HTML performance report.

    Args:
        report: Computed performance metrics.
        nav_series: DataFrame with date, nav, daily_return, benchmark_return columns.
        attribution: Optional factor attribution results.
        weight_history: Optional DataFrame with date + per-symbol weight columns.
        output_path: Path to write the HTML file.

    Returns:
        Path to the written HTML file.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)

    nav_chart = _render_nav_chart(nav_series)
    drawdown_chart = _render_drawdown_chart(nav_series)
    attribution_section = _render_attribution_section(attribution) if attribution else ""
    weight_section = _render_weight_section(weight_history) if weight_history is not None else ""

    html = f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>Performance Report — {report.portfolio_id}</title>
<style>
{_css()}
</style>
</head>
<body>
<div class="container">
<h1>Performance Report</h1>
<p class="subtitle">{report.portfolio_id} &mdash; {report.start_date} to {report.end_date}</p>

<section class="metrics">
<h2>Summary Metrics</h2>
{_render_metrics_table(report)}
</section>

<section class="chart-section">
<h2>Portfolio NAV</h2>
{nav_chart}
</section>

<section class="chart-section">
<h2>Drawdown</h2>
{drawdown_chart}
</section>

{attribution_section}
{weight_section}

<footer>
<p>Generated by alpha-holdings. Free-data limitations apply.</p>
</footer>
</div>
</body>
</html>"""

    output_path.write_text(html, encoding="utf-8")
    return output_path


